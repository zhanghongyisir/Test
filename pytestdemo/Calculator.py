
class Calculator:

    def add(self, a, b):
        '''
        计算功能：加法
        :param a: 加数
        :param b: 加数
        :return:
        '''
        return a + b


    def div(self, a, b):
        '''
        计算功能：除法
        :param a: 被除数
        :param b:除数
        :return:
        '''
        return a / b
